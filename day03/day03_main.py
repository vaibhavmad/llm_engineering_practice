"""
1. Base, Chat and Reasoning Models: Uderstanding LLM Types

LLMs come in 3 flavors:
1. Base: LLM just takes the sequence of the info and predicts what comes next. This is like the one we have in our phones keypad. Like if we have predictive messaging on, then on type one word, we get the next predictions. If we keep adding this, we will be using this type of model. Base models are better for fine tuning to learn a new skill. When we want a model to behave in a particular manner by using data set that we provide it with, we should use base models.

2. Chat/Instruct: This is a variant of the base LLM, where base models were trained on large datasets and these models then started responding in the form of chat models as we see today. These are great for content generation and creative tasks (which is a form of content generation). These models are faster and cheaper when compared with reasoning models.

3. Reasoning Model: The chat models were asked to thinking through, or asked to work in steps. These models think and then give output of the response. They decide how much to reason on the basis of the prompt that we gave it. These are great for problem solving. These are kind of the most expensive models, as they also spend tokens while thinking. 
"""

